package com.company;

public class Traveler {
    String name = "";
    String flight = "";
    String from = "";
    String to = "";
    String seat = "";
    String code = "";

    public Traveler (String Name, String Option, String From, String To, String Seat, String Code) {
        name = Name;
        flight = Option;
        from = From;
        to = To;
        seat = Seat;
        code = Code;
    }
}
